fn get_firefox_hitbox_angle(in_angle: f32) -> f32 {
    let angle_1 = in_angle * 57.2957795131 + 90.0;
    let angle_2 = 180.0 * (1.0 - ((180.0 - angle_1) / 180.0).powi(2)) - 90.0;
    if angle_2 < 0.0 {
        return angle_2 + 360.0;
    }
    return angle_2;
}



