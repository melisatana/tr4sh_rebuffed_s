static mut NESS_DASHATTACK_HOLD : [bool; 8] = [false; 8];
static mut NESS_DASHATTACK_HOLD_PARTICLE_FRAME : [i32; 8] = [0; 8];

// A Once-Per-Fighter-Frame that only applies to ness. Neat!
unsafe extern "C" fn ness_frame(fighter: &mut L2CFighterCommon) {
    unsafe {
        global_fighter_frame(fighter);
        let entry_id = WorkModule::get_int(fighter.module_accessor, *FIGHTER_INSTANCE_WORK_ID_INT_ENTRY_ID) as usize;
        let status = StatusModule::status_kind(fighter.module_accessor);

        //println!("It'sa me, Ness, OK");

        if NESS_DASHATTACK_HOLD[entry_id] {
            if [*FIGHTER_STATUS_KIND_ATTACK_DASH].contains(&status) {
                macros::SET_SPEED_EX(fighter, 1.2, 0, *KINETIC_ENERGY_RESERVE_ATTRIBUTE_MAIN);
                if ControlModule::check_button_on(fighter.module_accessor, *CONTROL_PAD_BUTTON_ATTACK) == false {
                    NESS_DASHATTACK_HOLD[entry_id] = false;
                    NESS_DASHATTACK_HOLD_PARTICLE_FRAME[entry_id] = 0;
                    MotionModule::set_rate(fighter.module_accessor, 1.0);
                }
                else {
                    NESS_DASHATTACK_HOLD_PARTICLE_FRAME[entry_id] += 1;
                    if NESS_DASHATTACK_HOLD_PARTICLE_FRAME[entry_id] >= 4 {
                        macros::EFFECT(fighter, Hash40::new("ness_psi_rush"), Hash40::new("top"), 0, 5, 16, 0, 0, 0, 1.4, 2, 2, 2, 0, 0, 0, true);
                        macros::PLAY_SE(fighter, Hash40::new("se_ness_pk_s"));
                        NESS_DASHATTACK_HOLD_PARTICLE_FRAME[entry_id] = 0;
                    }
                }
            }
            else {
                NESS_DASHATTACK_HOLD[entry_id] = false;
            }
        }

        if [*FIGHTER_STATUS_KIND_SPECIAL_S].contains(&status) {
            crate::custom::fastfall_helper(fighter);
        }

        if [*FIGHTER_NESS_STATUS_KIND_SPECIAL_HI_HOLD].contains(&status) {
            if ControlModule::check_button_on(fighter.module_accessor, *CONTROL_PAD_BUTTON_GUARD) {
                StatusModule::change_status_request_from_script(fighter.module_accessor, *FIGHTER_NESS_STATUS_KIND_SPECIAL_HI_END, false);
            }
        }

        if status == *FIGHTER_NESS_STATUS_KIND_SPECIAL_HI_END {    
            if WorkModule::get_int(fighter.module_accessor, *FIGHTER_INSTANCE_WORK_ID_INT_JUMP_COUNT) >= 1 {
                WorkModule::enable_transition_term(fighter.module_accessor, *FIGHTER_STATUS_TRANSITION_TERM_ID_CONT_ESCAPE_AIR);
                fighter.sub_transition_group_check_air_escape();
            }
        }


    }
}

