static mut ROBOT_SLOW_BACK_AIR : [bool; 8] = [false; 8];
static mut ROBOT_SLOW_BACK_AIR_IN_SLOW : [bool; 8] = [false; 8];
static mut ROBOT_SLOW_BACK_AIR_CAN_CANCEL : [bool; 8] = [false; 8];

static mut ROBOT_DTHROW_BURY_RECHARGE_TIMER : [i32; 8] = [0; 8];
static ROBOT_DTHROW_BURY_COOLDOWN : i32 = 480; 

// A Once-Per-Fighter-Frame that only applies to R.O.B.
#[fighter_frame( agent = FIGHTER_KIND_ROBOT )]
fn robot_frame(fighter: &mut L2CFighterCommon) {
    unsafe {
        let entry_id = WorkModule::get_int(fighter.module_accessor, *FIGHTER_INSTANCE_WORK_ID_INT_ENTRY_ID) as usize;
        let status = StatusModule::status_kind(fighter.module_accessor);
        

        println!("01001001 00100000 01100001 01101101 00100000 01110011 01100101 01100011 01110010 01100101 01110100 01101100 01111001 00100000 01100001 01101110 00100000 01100101 01110110 01101001 01101100 00100000 01110010 01101111 01100010 01101111 01110100");
    
        if ROBOT_DTHROW_BURY_RECHARGE_TIMER[entry_id] > 0 {
            ROBOT_DTHROW_BURY_RECHARGE_TIMER[entry_id] -= 1;
            if ROBOT_DTHROW_BURY_RECHARGE_TIMER[entry_id] == 0 {
                macros::EFFECT(fighter, Hash40::new("sys_smash_flash"), Hash40::new("top"), 0, 9.0, 0, 0, 0, 0, 1.4, 0, 0, 0, 0, 0, 0, true);
                macros::LAST_EFFECT_SET_COLOR(fighter, 2.5, 2.5, 1.0);
            }
        }
        if sv_information::is_ready_go() == false || [*FIGHTER_STATUS_KIND_WIN, *FIGHTER_STATUS_KIND_LOSE, *FIGHTER_STATUS_KIND_DEAD].contains(&status) {
			ROBOT_DTHROW_BURY_RECHARGE_TIMER[entry_id] = 0;
		}

        if ROBOT_SLOW_BACK_AIR[entry_id] && status == *FIGHTER_STATUS_KIND_ATTACK_AIR && ROBOT_SLOW_BACK_AIR_CAN_CANCEL[entry_id] && ControlModule::check_button_on(fighter.module_accessor, *CONTROL_PAD_BUTTON_ATTACK) == false {
            ROBOT_SLOW_BACK_AIR[entry_id] = false;
            if ROBOT_SLOW_BACK_AIR_IN_SLOW[entry_id] {
                MotionModule::set_rate(fighter.module_accessor, 1.0);
            }
        }
        if ROBOT_SLOW_BACK_AIR[entry_id] && status != *FIGHTER_STATUS_KIND_ATTACK_AIR {
            ROBOT_SLOW_BACK_AIR[entry_id] = false;
        }

        if [*FIGHTER_STATUS_KIND_SPECIAL_HI, *FIGHTER_ROBOT_STATUS_KIND_SPECIAL_HI_KEEP].contains(&status) {
            if MotionModule::frame(fighter.module_accessor) >= (10.0) {
                WorkModule::enable_transition_term(fighter.module_accessor, *FIGHTER_STATUS_TRANSITION_TERM_ID_CONT_ATTACK_AIR);
                WorkModule::enable_transition_term(fighter.module_accessor, *FIGHTER_STATUS_TRANSITION_TERM_ID_CONT_SPECIAL_N);
                WorkModule::enable_transition_term(fighter.module_accessor, *FIGHTER_STATUS_TRANSITION_TERM_ID_CONT_SPECIAL_S);
                WorkModule::enable_transition_term(fighter.module_accessor, *FIGHTER_STATUS_TRANSITION_TERM_ID_CONT_SPECIAL_LW);
                WorkModule::enable_transition_term(fighter.module_accessor, *FIGHTER_STATUS_TRANSITION_TERM_ID_CONT_ESCAPE_AIR);
                fighter.sub_transition_group_check_air_attack();
                fighter.sub_transition_group_check_air_special();
                fighter.sub_transition_group_check_air_escape();
            
            }
        }

    }
}

